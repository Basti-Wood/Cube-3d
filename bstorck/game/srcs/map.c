int	**init_map(int map_width, int map_height)
{
	int	x = 0;
	int	y = 0;
	int** map = NULL;
	map = (int**)malloc(map_height * sizeof(int*));
	y = -1;
	while (map_height - ++y)
		map[y] = (int*)malloc(map_width * sizeof(int));
	y = -1;
	while (map_height - ++y)
	{
		x = -1;
		while (map_width - ++x)
			map[y][x] = world[y][x];
	}
	return (map);
}
